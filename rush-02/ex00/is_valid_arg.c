#include "./ft.h"

int		is_valid_arg(char *str)
{
	if (ft_strlen(str) != 1 && *str == '0')
	{
		return (0);
	}
	while (*str != '\0')
	{
		if (*str < '0' || *str > '9')
		{
			return (0);
		}
		str++;
	}
	return (1);
}
